import torch
import torch.nn as nn

from utils.registry import NETWORK_REGISTRY


def double_conv(in_channels, out_channels):
    return nn.Sequential(
        nn.Conv2d(in_channels, out_channels, 3, padding=1),
        nn.LeakyReLU(0.1),
        nn.Conv2d(out_channels, out_channels, 3, padding=1),
        nn.LeakyReLU(0.1),
    )


@NETWORK_REGISTRY.register()
class UNet(nn.Module):

    def __init__(self, in_channels=3, out_channels=2, hidden_units=[64,128,256,512]):
        super().__init__()

        self.dconv_down1 = double_conv(in_channels, hidden_units[0])
        self.dconv_down2 = double_conv(hidden_units[0], hidden_units[1])
        self.dconv_down3 = double_conv(hidden_units[1], hidden_units[2])
        self.dconv_down4 = double_conv(hidden_units[2], hidden_units[3])

        self.maxpool = nn.MaxPool2d(2)
        self.upsample = nn.Upsample(scale_factor=2, mode='bilinear', align_corners=True)

        self.dconv_up3 = double_conv(hidden_units[3] + hidden_units[2], hidden_units[2])
        self.dconv_up2 = double_conv(hidden_units[2] + hidden_units[1], hidden_units[1])
        self.dconv_up1 = double_conv(hidden_units[1] + hidden_units[0], hidden_units[0])

        self.conv_last = nn.Conv2d(hidden_units[0], out_channels, 1)

    def forward(self, x):
        
        conv1 = self.dconv_down1(x)
        x = self.maxpool(conv1)

        conv2 = self.dconv_down2(x)
        x = self.maxpool(conv2)

        conv3 = self.dconv_down3(x)
        x = self.maxpool(conv3)

        x = self.dconv_down4(x)

        x = self.upsample(x)
        x = torch.cat([x, conv3], dim=1)

        x = self.dconv_up3(x)
        x = self.upsample(x)
        x = torch.cat([x, conv2], dim=1)

        x = self.dconv_up2(x)
        x = self.upsample(x)
        x = torch.cat([x, conv1], dim=1)

        x = self.dconv_up1(x)

        out = self.conv_last(x)

        return out


